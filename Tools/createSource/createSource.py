	k_list = to_byte_list(key)

	# expand key
	while len(k_list) < len(b_list):
		k_list = k_list + k_list

	# contract key to right size
	if len(k_list) > len(b_list):
		k_list = k_list[:len(b_list)]

	o_list = []

	for ind in range(len(b_list)):
		o_list.append((b_list[ind] ^ k_list[ind]) & 0xff)

	# unsalt the hash
	half_len = len(o_list)//2
	first_half = o_list[:half_len]
	last_half = o_list[half_len:]

	o_list = last_half + first_half

	o_hash = ""
	for o in o_list:
		o_hash += chr(o)

	return o_hash


